const mongoose = require("mongoose");
const { ObjectId } = mongoose.Schema; // means including some other mongoose Schema
// in this case we are using "Category" Schema ,, we will told by using "ref" keyword

const productSchema = new mongoose.Schema(
  {
    name: {
      type: String,
      trim: true,
      required: true,
      maxlength: 32,
    },
    description: {
      type: String,
      trim: true,
      required: true,
      maxlength: 2000,
    },
    price: {
      type: Number,
      required: true,
      maxlength: 32,
      trim: true,
    },
    category: {
      type: ObjectId,
      ref: "Category", // connecting "Category" Schema
      //  to use this we have to use .populate() method, refer to "medium article" on mongoose populate
      required: true,
    },
    stock: {
      type: Number,
    },
    sold: {
      type: Number,
      default: 0,
    },
    photo: {
      data: Buffer,
      contentType: String,
    },
  },
  { timestamps: true }
);

module.exports = mongoose.model("Product", productSchema);
